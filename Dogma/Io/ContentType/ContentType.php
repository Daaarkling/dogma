    /**
     * @param string $extension
     * @return \Dogma\Io\ContentType\ContentType|null
     */
    public static function findByExtension(string $extension)
    {
        if (isset(self::$extensions[$extension])) {
            return self::get(self::$extensions[$extension]);
        } else {
            return null;
        }
    }

