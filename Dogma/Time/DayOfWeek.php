/**
 * Day of week as defined in ISO-8601 (1 for Monday through 7 for Sunday)
 */
class DayOfWeek extends \Dogma\Enum
{

    const MONDAY = 1;
    const TUESDAY = 2;
    const WEDNESDAY = 3;
    const THURSDAY = 4;
    const FRIDAY = 5;
    const SATURDAY = 6;
    const SUNDAY = 7;

}
