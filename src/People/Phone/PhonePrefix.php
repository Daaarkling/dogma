    /**
     * @return \Dogma\Country\Country[]
     */
    public function getCountries(): array
    {
        return array_map(function (string $countryCode) {
            return Country::get($countryCode);
        }, self::$countries[$this->getValue()]);
    }

    public function getByCountry(Country $country): ?self
    {
        foreach (self::$countries as $prefix => $countries) {
            if (Arr::contains($countries, $country->getValue())) {
                return self::get($prefix);
            }
        }
        return null;
    }

